-- create schema for tools_shop
CREATE SCHEMA tools_shop;

-- create tables for tools_shop
CREATE TABLE tools_shop.costs (
    created_at TIMESTAMP,
    costs      NUMERIC
);

CREATE TABLE tools_shop.users (
    PRIMARY KEY (user_id),
    user_id    SERIAL,
    first_name TEXT,
    last_name  TEXT,
    email      TEXT,
    created_at DATE
);

CREATE TABLE tools_shop.items (
    PRIMARY KEY (item_id),
    item_id   SERIAL,
    category  TEXT,
    item_name TEXT,
    price     NUMERIC
);

CREATE TABLE tools_shop.orders (
    PRIMARY KEY (order_id),
    order_id   SERIAL,
    user_id    INTEGER,
    total_amt  NUMERIC,
    items_cnt  INTEGER,
    created_at TIMESTAMP,
    paid_at    TIMESTAMP
);

CREATE TABLE tools_shop.order_x_item (
    PRIMARY KEY (order_id, item_id),
    order_id INTEGER,
    item_id  INTEGER
);

CREATE TABLE tools_shop.events (
    PRIMARY KEY (event_id),
    event_id   TEXT,
    event_time TIMESTAMP,
    event_name TEXT,
    user_id    INTEGER,
    platform   TEXT,
    referrer   TEXT
);

CREATE TABLE tools_shop.event_x_event_parameter (
    event_id        TEXT,
    parameter_name  TEXT,
    parameter_value INTEGER
);

